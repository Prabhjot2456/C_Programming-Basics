// type casting 
// upcasting
// downcasting

//Explicit Fxn
#include<stdio.h>
int main(){
    int a = 10;
    float b = 10.23;
    float c;
    c = (float) a;
    printf("%f", c);
}

// Implicit Fxn
#include <stdio.h>
int main() {
    int x=10;
    float y=x;
    printf("%f", y);
}